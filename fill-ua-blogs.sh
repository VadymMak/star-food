## Огляд ринку соняшникової олії

Європейський ринок соняшникової олії оцінюється приблизно в $9,36 мільярда у 2025 році з прогнозом зростання до $12,6 мільярда до 2034 року. Запаси олії знаходяться на історичних мінімумах, а покупці активно шукають нових постачальників.

