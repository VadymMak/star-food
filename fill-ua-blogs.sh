## Що таке Incoterms і навіщо вони потрібні

Incoterms (International Commercial Terms) — це міжнародні правила, які визначають хто відповідає за товар на кожному етапі доставки: хто платить за транспорт, страхування і митне оформлення.

Для покупців продуктів харчування три найважливіші умови — це FOB, CIF та DAP. Розуміння різниці між ними допоможе вам обрати найвигідніший варіант.

## FOB — Free On Board (Франко-борт)

**Що означає:** Продавець доставляє товар до порту відвантаження та завантажує на судно. Далі — відповідальність покупця.

**Хто за що платить:**
- Продавець: виробництво, доставка до порту, завантаження
- Покупець: морський фрахт, страхування, митне оформлення, доставка від порту

**Коли обирати FOB:**
- У вас є власний логістичний партнер або брокер
- Ви маєте кращі тарифи на доставку
- Ви імпортуєте великі обсяги регулярно
- Ви хочете контролювати процес доставки

