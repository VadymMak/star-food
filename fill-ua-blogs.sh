## Основна різниця

Рафінована та нерафінована (сира) соняшникова олія — це два принципово різних продукти, хоча виготовляються з одної сировини. Розуміння різниці критично важливе для оптових покупців.

**Нерафінована олія** — це олія першого пресування без хімічної обробки. Зберігає натуральний колір, запах та всі поживні речовини. Використовується як сировина для рафінації або як кулінарна олія преміум-класу.

**Рафінована олія** — це олія, що пройшла повний цикл очищення: дегумування, нейтралізацію, відбілювання та дезодорацію. Результат — прозора, нейтральна за смаком олія, ідеальна для приготування їжі.

## Порівняльна таблиця

| Параметр | Рафінована | Нерафінована |
|----------|-----------|-------------|
| Кислотність | < 0,1% | 1-3% |
| Колір | Світло-жовтий | Темно-жовтий, золотистий |
| Запах | Нейтральний | Характерний, насіннєвий |
| Смак | Нейтральний | Виражений |
| Температура димоутворення | 230°C | 160-170°C |
| Вітамін E | Знижений | Збережений |
| Термін придатності | 12-18 місяців | 6-9 місяців |
| Ціна FOB | $1 050-1 100/тон | $900-950/тон |

## Процес рафінації

Рафінація включає п'ять основних етапів:

**1. Дегумування** — видалення фосфоліпідів та слизистих речовин за допомогою води або кислоти.

**2. Нейтралізація** — видалення вільних жирних кислот лугом (NaOH), що знижує кислотність до 0,05-0,1%.

**3. Відбілювання** — пропускання через активовану глину для видалення пігментів, що надає олії світлий колір.

**4. Вінтеризація** — охолодження для видалення воскоподібних речовин, що забезпечує прозорість при зберіганні.

**5. Дезодорація** — обробка паром при високій температурі для видалення запаху та смаку.

## Де використовується кожен тип

### Рафінована олія
- Роздрібна торгівля (супермаркети, магазини)
- Ресторани та заклади HORECA
- Харчова промисловість (випічка, консерви, соуси)
- Виробництво маргарину та кулінарних жирів
- Фритюр та смаження

### Нерафінована олія
- Преміум кулінарна олія (для салатів та холодних страв)
- Сировина для рафінаційних заводів
- Виробництво косметики та фармацевтики
- Технічне використання (лаки, фарби)
- Виробництво біодизеля

## Цінова різниця

Нерафінована олія зазвичай дешевша на $100-200 за тонну, оскільки не потребує витрат на рафінацію. Однак для більшості комерційних застосувань потрібна саме рафінована олія.

Якщо ви купуєте нерафіновану олію для подальшої переробки — ви заощаджуєте на закупівлі, але повинні мати власне рафінаційне обладнання.

## Що обрати для вашого бізнесу

**Обирайте рафіновану, якщо:**
- Ви дистриб'ютор або роздрібна мережа
- Ваші клієнти — ресторани та кафе
- Вам потрібна олія для кулінарної обробки при високих температурах
- Важливий тривалий термін придатності

**Обирайте нерафіновану, якщо:**
- Ви маєте власний рафінаційний завод
- Ви виробляєте преміум-продукцію з натуральних інгредієнтів
