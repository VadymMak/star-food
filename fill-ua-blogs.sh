## Типи соняшникової олії

### Рафінована соняшникова олія

Найпоширеніший тип для роздрібної торгівлі та харчової промисловості. Проходить повний цикл очищення: дегумування, нейтралізація, відбілювання, дезодорація. Результат — прозора олія світло-жовтого кольору без запаху, ідеальна для приготування їжі.

**Характеристики:**
- Кислотність: максимум 0,1%
- Колір: світло-жовтий, прозорий
- Температура димоутворення: 230°C
- Термін придатності: 12-18 місяців

### Нерафінована (сира) соняшникова олія

Олія першого холодного пресування без хімічної обробки. Має насичений жовтий колір і характерний запах насіння. Використовується як сировина для рафінації або як кулінарна олія преміум-класу.

**Характеристики:**
- Кислотність: 1-3%
- Колір: темно-жовтий до золотистого
- Зберігає більше вітаміну E та поживних речовин
- Коротший термін придатності: 6-9 місяців

### Високоолеїнова соняшникова олія

Спеціальний сорт з високим вмістом олеїнової кислоти (80%+). Ідеальна для фритюру в HORECA завдяки підвищеній стабільності при високих температурах.

## Класи якості

| Клас | Кислотність | Призначення |
|------|-------------|-------------|
| Перший (Grade 1) | < 0,1% | Роздріб, HORECA |
| Другий (Grade 2) | 0,1-0,3% | Харчова промисловість |
| Третій (Grade 3) | 0,3-1,0% | Технічне використання |

Ми в UB Market працюємо виключно з олією першого класу (Grade 1) для забезпечення найвищої якості.

## Фактори ціноутворення

Ціна соняшникової олії залежить від кількох ключових факторів:

**Сезонність.** Ціни зазвичай найнижчі одразу після збору врожаю (жовтень-листопад) і зростають протягом весни та літа.

**Обсяг замовлення.** Великі замовлення (20+ тон) отримують суттєві знижки порівняно з малими партіями. Різниця може досягати $100-250 на тонну.

**Умови доставки.** FOB (франко-борт) — базова ціна без доставки. CIF (вартість, страхування, фрахт) включає доставку до порту покупця. DAP (доставка до місця) — повна ціна з доставкою до вашого складу.

**Тип пакування.** Наливна олія (bulk) — найдешевша. PET пляшки додають $150-250/тон до вартості.

